///////////////////////////////////////////////////////////////////////////////
//
// Drop Stream
//
///////////////////////////////////////////////////////////////////////////////

//// IMPORTS //////////////////////////////////////////////////////////////////

import { domEventBus } from './domEvent';

//// HELPERS //////////////////////////////////////////////////////////////////

const dropPredicate = e => e.type === 'drop'

///////////////////////////////////////////////////////////////////////////////

// :: EventStream
const dropStream = domEventBus
  .filter(dropPredicate)
  .doAction('.preventDefault');

//// EXPORTS //////////////////////////////////////////////////////////////////

export default dropStream;

