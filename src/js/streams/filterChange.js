///////////////////////////////////////////////////////////////////////////////
//
// Filter Change Stream
//
///////////////////////////////////////////////////////////////////////////////

//// IMPORTS //////////////////////////////////////////////////////////////////

import { CHANGE_FILTER_TEXT } from '../../../constants';
import { domEventBus } from './domEvent';

//// HELPERS //////////////////////////////////////////////////////////////////

// :: Event -> Boolean
const filterChangePredicate = e => e.event === CHANGE_FILTER_TEXT;

///////////////////////////////////////////////////////////////////////////////

const filterChangeStream = domEventBus
  .filter(filterChangePredicate)
  .map('.text');

export default filterChangeStream;
