///////////////////////////////////////////////////////////////////////////////
//
// DOM Delegator
//
///////////////////////////////////////////////////////////////////////////////

// Enables dom events to be handled by the views.

//// IMPORT ///////////////////////////////////////////////////////////////////

import Delegator from 'dom-delegator';

///////////////////////////////////////////////////////////////////////////////

const delegator = new Delegator();

delegator.listenTo('click');
delegator.listenTo('mouseover');
delegator.listenTo('mouseout');

