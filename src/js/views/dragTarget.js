//// IMPORTS //////////////////////////////////////////////////////////////////

import h from 'virtual-dom/h';
import classnames from 'classnames';
import { domEventBus } from '../streams/domEvent';

//// HELPERS //////////////////////////////////////////////////////////////////

let handleEvent = e => domEventBus.push(e);

//// COMPONENT ////////////////////////////////////////////////////////////////

// Object model -> Virtual DOM
let modelToVDOM = model => {

  let className = classnames('drag-target', {
    'drag-target--hover' : model.isOver
  });

  return h('section',
    {
      className,
      'ev-dragenter' : handleEvent,
      'ev-dragleave' : handleEvent,
      'ev-dragover'  : handleEvent,
      'ev-drop'      : handleEvent,
      'ev-click'     : handleEvent
    },
    [
      h('h2', className)
    ]
  );

};

//// EXPORTS //////////////////////////////////////////////////////////////////

export default modelToVDOM;

